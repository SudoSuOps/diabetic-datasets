const $ = s => document.querySelector(s);
const esc = s => String(s ?? "").replace(/[&<>]/g, c => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;" }[c]));
const num = n => Number(n || 0).toLocaleString();
let CAT = [], filterCat = "all", query = "";

const STATUS = {
  available: { cls: "available", label: "● download sample" },
  "deed-verified": { cls: "deed", label: "● deed-verified · releasing" },
  "open-source": { cls: "open", label: "● open · source link" },
  importing: { cls: "deed", label: "● importing" },
};

function cardHtml(d) {
  const st = STATUS[d.status] || STATUS.importing;
  return `<div class="card" data-slug="${esc(d.slug)}">
    <div class="row"><span class="cat">${esc(d.category)}</span>
      <span class="badge ${esc(d.tier)}">${esc(d.tier)}</span></div>
    <h3>${esc(d.name)}</h3>
    <p class="desc">${esc(d.description)}</p>
    ${d.deed_backed ? `<div class="deedtag">🏷️ Deed-backed · Tribunal-verified · Merkle+Hedera</div>` : ""}
    <div class="meta">
      <span>${d.rows ? num(d.rows) + " rows" : esc(d.size)}</span><span>${esc(d.format)}</span>
      <span class="status ${st.cls}">${st.label}</span>
    </div></div>`;
}

async function load() {
  const c = await (await fetch("catalog.json", { cache: "no-store" })).json();
  CAT = c.datasets;
  $("#flagship").innerHTML = CAT.filter(d => d.flagship).map(cardHtml).join("");
  const cats = ["all", ...new Set(CAT.map(d => d.category))];
  $("#filters").innerHTML = cats.map(x =>
    `<button data-cat="${esc(x)}" class="${x === "all" ? "on" : ""}">${x === "all" ? "All" : esc(x)}</button>`).join("");
  document.querySelectorAll("#filters button").forEach(b => b.onclick = () => {
    filterCat = b.dataset.cat;
    document.querySelectorAll("#filters button").forEach(x => x.classList.toggle("on", x === b));
    render();
  });
  $("#search").addEventListener("input", e => { query = e.target.value.toLowerCase(); render(); });
  render();
  bindCards();
}

function match(d) {
  if (filterCat !== "all" && d.category !== filterCat) return false;
  if (!query) return true;
  return (d.name + " " + d.description + " " + d.category).toLowerCase().includes(query);
}

function render() {
  const list = CAT.filter(match);
  $("#grid").innerHTML = list.length ? list.map(cardHtml).join("") : `<p class="muted">No datasets match that search.</p>`;
  bindCards();
}
function bindCards() {
  document.querySelectorAll(".card").forEach(c => c.onclick = () => openDetail(c.dataset.slug));
}

function openDetail(slug) {
  const d = CAT.find(x => x.slug === slug);
  if (!d) return;
  const schema = `<table class="schema">${d.schema.map(f =>
    `<tr><td><code>${esc(f.field)}</code></td><td class="muted">${esc(f.type)}</td></tr>`).join("")}</table>`;
  const samples = d.samples.map(s => esc(JSON.stringify(s, null, 2))).join("\n\n");
  const deed = d.deed_backed ? `
    <h4>🏷️ Title deed</h4>
    <div class="deedbox">
      <div><b>Origin</b> — generated by <code>${esc(d.deed.writer_model)}</code></div>
      <div><b>Quality</b> — ${esc(d.deed.tribunal)}</div>
      <div><b>Trust</b> — ${esc(d.deed.anchor)}</div>
      <div class="muted">Provenance you can verify. Every pair carries its own deed.</div>
    </div>` : `<h4>Provenance</h4><p class="muted">${esc(d.provenance)}</p>`;
  let action;
  const full = d.full_download
    ? `<a class="dlbtn full" href="${esc(d.full_download.url)}" download>⬇ Download full set · ${esc(d.full_download.size)} · ${num(d.full_download.rows)} rows</a>`
    : "";
  if (d.full_download && d.sample_download)
    action = `${full}<a class="dlbtn ghost" href="${esc(d.sample_download)}" download>⬇ Sample (12 rows)</a><span class="lic">${esc(d.license)}</span>`;
  else if (d.status === "available" && d.sample_download)
    action = `<a class="dlbtn" href="${esc(d.sample_download)}" download>⬇ Download sample (.jsonl)</a><span class="lic">${esc(d.license)}</span>`;
  else if (d.status === "open-source")
    action = `<a class="dlbtn" href="${esc(d.source_url)}" target="_blank" rel="noopener">↗ Get it from the source</a><span class="lic">${esc(d.license)} · re-hosted only with attribution</span>`;
  else
    action = `<a class="notify" href="mailto:build@opendiabetic.com?subject=Notify me: ${esc(d.name)}">🔔 Notify me — releasing soon</a><span class="lic">Deed-backed · ${esc(d.license)}</span>`;
  $("#detailBody").innerHTML = `
    <span class="badge ${esc(d.tier)}">${esc(d.tier)}</span> <h2>${esc(d.name)}</h2>
    <p class="sub">${esc(d.category)} — ${esc(d.description)}</p>
    <div class="kv">
      <div class="k"><b>${d.rows ? num(d.rows) : "—"}</b><span>rows</span></div>
      <div class="k"><b>${esc(d.format)}</b><span>format</span></div>
      <div class="k"><b>${esc(d.size)}</b><span>size</span></div>
      <div class="k"><b class="${d.status === "available" ? "green" : ""}">${esc(d.status)}</b><span>status</span></div>
    </div>
    ${deed}
    <h4>Schema</h4>${schema}
    <h4>Real sample rows (verify before you cook)</h4><pre class="sample">${samples}</pre>
    <h4>SHA-256</h4><p class="hash">${esc(d.sha256)}</p>
    <div class="dlbar">${action}</div>`;
  $("#detail").classList.remove("hide");
  document.body.style.overflow = "hidden";
}
function closeDetail() { $("#detail").classList.add("hide"); document.body.style.overflow = ""; }
$("#closeDetail").onclick = closeDetail;
$("#detail").addEventListener("click", e => { if (e.target.id === "detail") closeDetail(); });
document.addEventListener("keydown", e => { if (e.key === "Escape") closeDetail(); });

load();
